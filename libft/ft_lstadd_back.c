#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	struct s_list	*last;

	if (!alst || !new)
		return ;
	if (!*alst)
	{
		*alst = new;
		return ;
	}
	last = ft_lstlast(*alst);
	last->next = new;
}
