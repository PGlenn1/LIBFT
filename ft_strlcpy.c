#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t			i;
	unsigned int	len_src;

	if (size == 0)
		return (ft_strlen((char *)src));
	i = 0;
	while (src[i] && (i < (size - 1)))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	len_src = ft_strlen((char *)src);
	return (len_src);
}
